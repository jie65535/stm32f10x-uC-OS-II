// 串口1接收中断
void USART1_IRQHandler(void)
{
	// 缓冲区
	static uint8_t str[128];
	// 字符串长度
	static uint8_t length = 0;
	// 进入中断
	OSIntEnter();
	
	if (USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)
	{
		str[length] = USART_ReceiveData(USART1);
		// 如果是换行或者溢出则结束本次接收
		if (str[length] == '\n' || str[length] == '\0' || length == 127)
		{
			str[length] = '\0';
			// 通过邮箱发送数据
			OSMboxPost(pMailBox, str);
			// 长度重置
			length = 0;
		}
		else // 否则继续接收
			++length;
	}
	// 退出中断
	OSIntExit();
}


